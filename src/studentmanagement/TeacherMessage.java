    private void SendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendActionPerformed
        String studentID = StudentID.getText().trim();
    String message = jTextArea1.getText().trim();

    if (studentID.isEmpty() || message.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in both the student ID and message fields.");
        return;
    }

    try {
        // Önce öğrenci var mı kontrol et
        String checkSql = "SELECT * FROM RegisterStudent WHERE StudentID = ?";
        pst = con.prepareStatement(checkSql);
        pst.setString(1, studentID);
        rs = pst.executeQuery();

        if (!rs.next()) {
            JOptionPane.showMessageDialog(this, "There is no such student. Please enter a valid ID.");
            return;
        }

        // Sonra mesajı ekle
        String insertSql = "INSERT INTO TeacherAnnouncement (StudentID, Message) VALUES (?, ?)";
        pst = con.prepareStatement(insertSql);
        pst.setString(1, studentID);
        pst.setString(2, message);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "The message was sent successfully.");
        StudentID.setText("");
        jTextArea1.setText("");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_SendActionPerformed

